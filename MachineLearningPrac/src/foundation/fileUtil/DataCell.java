package foundation.fileUtil;

/**
 * @author nyc
 * 单元格类
 */
public class DataCell {
	//单元格名称
	private String name;
	//单元格值
	private String value;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
